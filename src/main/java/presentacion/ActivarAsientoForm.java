    public void buscarAsiento(){
        int idBuscar = Integer.parseInt(idTexto.getText());
        for (Asiento asiento : Main.asientos) {
            if (asiento != null && asiento.getIdAsiento() == idBuscar){
                activo = asiento;
                break;
            }
        }
        if (activo != null){
            numeroAsientoTexto.setText(activo.getNumeroAsiento());
        }else{
            metodos.msg("Asiento no encontrado por Id", "Mensaje de InformaciÃ³n", 1);
        }
        
    }
    
    public void activarAsiento(){ 
        
        int c = metodos.SIoNo("Â¿Desea activar este evento?","Consulta");
        if (c == JOptionPane.YES_OPTION){
            if (activo != null && activo.getDisponibilidad().equalsIgnoreCase("Activo")){
                metodos.msg("Este asiento ya ha sido activado previamente", "Mensaje de InformaciÃ³n", 1);
            }
            else if (activo != null && activo.getEstado().equalsIgnoreCase("Ocupado")){
                metodos.msg("Un asiento ocupado ya estÃ¡ activado por default", "Mensaje de InformaciÃ³n",1);
            }
            else if (activo != null){
                activo.setDisponibilidad("Activo");
                metodos.msg("Evento Activado Exitosamente","Mensaje de InformaciÃ³n",1);
            }else{
                metodos.msg("Debes buscar una asiento a activar", "Mensaje de InformaciÃ³n",1);
            }
        }
        else if (c == JOptionPane.NO_OPTION){
            
        }
    }
